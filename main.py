def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

# Example usage
num1 = 10
num2 = 5

sum_result = add(num1, num2)
subtract_result = subtract(num1, num2)

print(f"The sum of {num1} and {num2} is: {sum_result}")
print(f"The difference between {num1} and {num2} is: {subtract_result}")
